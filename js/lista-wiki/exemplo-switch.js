let letra = `a`;

switch(letra){
    case `a`:
        alert(`letra A`);
        break;
    case `e`:
        alert(`letra E`);
        break;
    case `i`:
        alert(`letra I`);
        break;
    case `o`:
        alert(`letra O`);
        break;
    case `u`:
        alert(`letra U`);
        break;
    default:
        alert(`Não é vogal`);
}